package ch.hevs.exception;

public class SeriesException extends RuntimeException {

	public SeriesException() {
		super();
	}

	public SeriesException(String arg0) {
		super(arg0);
	}

	public SeriesException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SeriesException(Throwable arg0) {
		super(arg0);
	}

}
